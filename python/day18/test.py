# 	有如下字符串：
# 	string  = """
# 	13866237416
# 	15699032456
# 	1381298012
# 	13866278399
# 	"""
# 	利用正则匹配出所有138开头的合法电话号码(11位)
import re
string  = """
13866237416
15699032456
1381298012
13866278399
"""
strings=string.split('\n')[1:5]
for i in strings:
    if re.match('^138[0-9]{8}$',i):
        print(i)

